#!/usr/bin/env python3
import argparse
import csv
import html
import math
import os
from collections import defaultdict


def parse_args():
    parser = argparse.ArgumentParser(description="Plot Stage 2 summary metrics.")
    parser.add_argument("--input", required=True, help="Input summary CSV generated by summarize_runs.py")
    parser.add_argument("--output-dir", required=True, help="Directory for SVG plots")
    parser.add_argument("--x", default="position_noise_std", help="X-axis column")
    parser.add_argument("--series", default="method_name", help="Series/group column")
    return parser.parse_args()


def as_number_or_text(value):
    try:
        return float(value)
    except (TypeError, ValueError):
        return value


def collect_points(rows, x_column, series_column, metric):
    grouped = defaultdict(list)
    for row in rows:
        if metric not in row or x_column not in row:
            continue
        try:
            y_value = float(row[metric])
        except (TypeError, ValueError):
            continue
        if not math.isfinite(y_value):
            continue
        grouped[row.get(series_column, "default")].append((as_number_or_text(row[x_column]), y_value))
    for series_name in grouped:
        grouped[series_name].sort(key=lambda item: item[0])
    return grouped


def value_range(values, default_min=0.0, default_max=1.0):
    if not values:
        return default_min, default_max
    lo = min(values)
    hi = max(values)
    if math.isclose(lo, hi):
        pad = 0.5 if math.isclose(lo, 0.0) else abs(lo) * 0.1
        return lo - pad, hi + pad
    pad = (hi - lo) * 0.08
    return lo - pad, hi + pad


def x_mapper(points, left, width):
    raw_values = []
    for series_points in points.values():
        raw_values.extend([item[0] for item in series_points])
    if not raw_values:
        return lambda _value: left, []
    if all(isinstance(value, float) for value in raw_values):
        lo, hi = value_range(raw_values)

        def map_numeric(value):
            return left + (float(value) - lo) / (hi - lo) * width

        tick_values = sorted(set(raw_values))
        return map_numeric, tick_values

    labels = sorted(set(str(value) for value in raw_values))
    denom = max(len(labels) - 1, 1)
    positions = {label: left + idx / denom * width for idx, label in enumerate(labels)}
    return lambda value: positions[str(value)], labels


def write_svg_plot(rows, x_column, series_column, metric, ylabel, output_path):
    points = collect_points(rows, x_column, series_column, metric)
    width = 820
    height = 460
    left = 84
    right = 26
    top = 34
    bottom = 72
    plot_width = width - left - right
    plot_height = height - top - bottom
    colors = ["#1f77b4", "#d62728", "#2ca02c", "#9467bd", "#ff7f0e", "#17becf"]

    y_values = [item[1] for series_points in points.values() for item in series_points]
    if metric in ("success_rate", "collision_rate"):
        y_min, y_max = 0.0, 1.0
    else:
        y_min, y_max = value_range(y_values)

    map_x, x_ticks = x_mapper(points, left, plot_width)

    def map_y(value):
        return top + (y_max - value) / (y_max - y_min) * plot_height

    lines = [
        '<?xml version="1.0" encoding="UTF-8"?>',
        '<svg xmlns="http://www.w3.org/2000/svg" width="{}" height="{}" viewBox="0 0 {} {}">'.format(width, height, width, height),
        '<rect width="100%" height="100%" fill="white"/>',
        '<text x="{}" y="22" font-family="Arial, sans-serif" font-size="16" font-weight="700">{}</text>'.format(left, html.escape(ylabel)),
    ]

    for idx in range(6):
        ratio = idx / 5.0
        y_value = y_min + (y_max - y_min) * ratio
        y = map_y(y_value)
        lines.append('<line x1="{:.1f}" y1="{:.1f}" x2="{:.1f}" y2="{:.1f}" stroke="#e5e7eb" stroke-width="1"/>'.format(left, y, left + plot_width, y))
        lines.append('<text x="{}" y="{:.1f}" font-family="Arial, sans-serif" font-size="11" text-anchor="end" fill="#374151">{:.3g}</text>'.format(left - 8, y + 4, y_value))

    lines.append('<line x1="{0}" y1="{1}" x2="{2}" y2="{1}" stroke="#111827" stroke-width="1.2"/>'.format(left, top + plot_height, left + plot_width))
    lines.append('<line x1="{0}" y1="{1}" x2="{0}" y2="{2}" stroke="#111827" stroke-width="1.2"/>'.format(left, top, top + plot_height))

    for tick in x_ticks:
        x = map_x(tick)
        label = "{:.3g}".format(tick) if isinstance(tick, float) else str(tick)
        lines.append('<line x1="{:.1f}" y1="{}" x2="{:.1f}" y2="{}" stroke="#111827" stroke-width="1"/>'.format(x, top + plot_height, x, top + plot_height + 5))
        lines.append('<text x="{:.1f}" y="{}" font-family="Arial, sans-serif" font-size="11" text-anchor="middle" fill="#374151">{}</text>'.format(x, top + plot_height + 22, html.escape(label)))

    lines.append('<text x="{}" y="{}" font-family="Arial, sans-serif" font-size="12" text-anchor="middle" fill="#111827">{}</text>'.format(left + plot_width / 2, height - 20, html.escape(x_column)))
    lines.append('<text x="20" y="{}" font-family="Arial, sans-serif" font-size="12" text-anchor="middle" fill="#111827" transform="rotate(-90 20,{})">{}</text>'.format(top + plot_height / 2, top + plot_height / 2, html.escape(ylabel)))

    for idx, (series_name, series_points) in enumerate(sorted(points.items())):
        if not series_points:
            continue
        color = colors[idx % len(colors)]
        coords = ["{:.1f},{:.1f}".format(map_x(x_value), map_y(y_value)) for x_value, y_value in series_points]
        lines.append('<polyline points="{}" fill="none" stroke="{}" stroke-width="2.2"/>'.format(" ".join(coords), color))
        for x_value, y_value in series_points:
            lines.append('<circle cx="{:.1f}" cy="{:.1f}" r="3.6" fill="{}"/>'.format(map_x(x_value), map_y(y_value), color))
        legend_y = top + 18 + idx * 18
        lines.append('<rect x="{}" y="{}" width="12" height="12" fill="{}"/>'.format(left + plot_width - 150, legend_y - 10, color))
        lines.append('<text x="{}" y="{}" font-family="Arial, sans-serif" font-size="12" fill="#111827">{}</text>'.format(left + plot_width - 132, legend_y, html.escape(series_name)))

    lines.append("</svg>")
    with open(output_path, "w", encoding="utf-8") as svg_file:
        svg_file.write("\n".join(lines))


def main():
    args = parse_args()
    os.makedirs(args.output_dir, exist_ok=True)
    with open(args.input, newline="") as csv_file:
        rows = list(csv.DictReader(csv_file))

    plots = [
        ("success_rate", "Success Rate", "success_rate.svg"),
        ("collision_rate", "Collision Rate", "collision_rate.svg"),
        ("min_distance_mean", "Mean Min Distance (m)", "min_distance_mean.svg"),
    ]
    for metric, ylabel, filename in plots:
        write_svg_plot(rows, args.x, args.series, metric, ylabel, os.path.join(args.output_dir, filename))

    print("Wrote plots to {}".format(args.output_dir))


if __name__ == "__main__":
    main()
