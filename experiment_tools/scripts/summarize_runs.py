#!/usr/bin/env python3
import argparse
import csv
import math
import os
from collections import defaultdict


DEFAULT_GROUP_BY = [
    "scenario_name",
    "method_name",
    "position_noise_std",
    "velocity_noise_std",
    "delay_ms",
    "dropout_prob",
    "random_seed",
]


def to_float(value):
    try:
        number = float(value)
        return number if math.isfinite(number) else math.nan
    except (TypeError, ValueError):
        return math.nan


def to_bool(value):
    if isinstance(value, bool):
        return value
    text = str(value).strip().lower()
    return text in ("1", "true", "yes", "success")


def finite_values(rows, key):
    values = [to_float(row.get(key, "")) for row in rows]
    return [value for value in values if math.isfinite(value)]


def mean(values):
    if not values:
        return math.nan
    return sum(values) / len(values)


def summarize_group(rows):
    successes = [to_bool(row.get("success", "")) for row in rows]
    collisions = [to_bool(row.get("collision", "")) for row in rows]
    min_distances = finite_values(rows, "min_distance")
    path_lengths = finite_values(rows, "path_length")
    travel_times = finite_values(rows, "travel_time")
    average_speeds = finite_values(rows, "average_speed")
    planning_times = finite_values(rows, "planning_time")
    planning_time_maxes = finite_values(rows, "planning_time_max")

    return {
        "run_count": len(rows),
        "success_rate": mean([1.0 if item else 0.0 for item in successes]),
        "collision_rate": mean([1.0 if item else 0.0 for item in collisions]),
        "min_distance_mean": mean(min_distances),
        "min_distance_min": min(min_distances) if min_distances else math.nan,
        "path_length_mean": mean(path_lengths),
        "travel_time_mean": mean(travel_times),
        "average_speed_mean": mean(average_speeds),
        "planning_time_mean": mean(planning_times),
        "planning_time_max": max(planning_time_maxes) if planning_time_maxes else math.nan,
    }


def parse_args():
    parser = argparse.ArgumentParser(description="Summarize Stage 2 Intent-MPC run CSV files.")
    parser.add_argument("--input", required=True, help="Input run CSV generated by experiment_metric_recorder.py")
    parser.add_argument("--output", required=True, help="Output summary CSV")
    parser.add_argument(
        "--group-by",
        default=",".join(DEFAULT_GROUP_BY),
        help="Comma-separated grouping columns. Default: %(default)s",
    )
    return parser.parse_args()


def main():
    args = parse_args()
    group_by = [item.strip() for item in args.group_by.split(",") if item.strip()]

    with open(args.input, newline="") as csv_file:
        rows = list(csv.DictReader(csv_file))

    groups = defaultdict(list)
    for row in rows:
        key = tuple(row.get(column, "") for column in group_by)
        groups[key].append(row)

    fieldnames = group_by + [
        "run_count",
        "success_rate",
        "collision_rate",
        "min_distance_mean",
        "min_distance_min",
        "path_length_mean",
        "travel_time_mean",
        "average_speed_mean",
        "planning_time_mean",
        "planning_time_max",
    ]

    os.makedirs(os.path.dirname(os.path.abspath(args.output)), exist_ok=True)
    with open(args.output, "w", newline="") as csv_file:
        writer = csv.DictWriter(csv_file, fieldnames=fieldnames)
        writer.writeheader()
        for key, group_rows in sorted(groups.items()):
            summary = {column: value for column, value in zip(group_by, key)}
            summary.update(summarize_group(group_rows))
            writer.writerow(summary)

    print("Wrote {} grouped rows to {}".format(len(groups), args.output))


if __name__ == "__main__":
    main()
